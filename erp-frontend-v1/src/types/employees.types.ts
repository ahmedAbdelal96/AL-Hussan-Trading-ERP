/**
 * Get employee full name
 */
export const getEmployeeFullName = (
  employee: EmployeeEntity | CreateEmployeeDto | UpdateEmployeeDto,
): string => {
  const parts = [
    "firstName" in employee ? employee.firstName : "",
    "middleName" in employee ? employee.middleName : "",
    "lastName" in employee ? employee.lastName : "",
  ].filter(Boolean);
  return parts.join(" ");
};

/**
 * Format employee number for display
 */
export const formatEmployeeNumber = (number: string): string => {
  if (!number) return "";
  return number.startsWith("EMP-") ? number : `EMP-${number}`;
};

/**
 * Calculate employment duration in days
 */
export const getEmploymentDuration = (
  hireDate: string,
  terminationDate?: string | null,
): number => {
  const endDate = terminationDate ? new Date(terminationDate) : new Date();
  const startDate = new Date(hireDate);
  const diff = endDate.getTime() - startDate.getTime();
  return Math.floor(diff / (1000 * 60 * 60 * 24));
};

/**
 * Format employment duration for display
 */
export const formatEmploymentDuration = (days: number): string => {
  const years = Math.floor(days / 365);
  const months = Math.floor((days % 365) / 30);

  if (years > 0) {
    return `${years} ${years === 1 ? "سنة" : "سنوات"}${
      months > 0 ? ` و ${months} شهر` : ""
    }`;
  }
  if (months > 0) {
    return `${months} ${months === 1 ? "شهر" : "شهور"}`;
  }
  return `${days} ${days === 1 ? "يوم" : "أيام"}`;
};

