/**
 * Salary History Entity - Immutable audit trail of salary changes
 * Created automatically whenever salary is updated via UpdateEmployeeSalaryUseCase
 */
export interface SalaryHistoryEntity {
  id: string;
  employeeId: string;
  /**
   * Salary amount BEFORE the change
   */
  baseSalaryBefore: number;

  /**
   * Salary amount AFTER the change
   */
  baseSalaryAfter: number;

  /**
   * Absolute change in salary (After - Before)
   * Computed field: can be negative for salary reductions
   */
  changeAmount?: number;

  /**
   * Percentage change: ((After - Before) / Before) * 100
   * Computed field: allows comparing salary changes across different salary levels
   * Example: 10% increase regardless of base amount
   */
  changePercentage?: number;

  /**
   * Computed field: true if changeAmount > 0
   */
  isRaise?: boolean;

  /**
   * Reason provided by the user for the salary change
   * Optional, max 500 characters
   * Examples: "Annual review", "Promotion", "Market adjustment", "Cost of living adjustment"
   */
  reason?: string | null;

  /**
   * Source of the salary change - who/what initiated it
   * - MANUAL: Changed via payroll module by HR
   * - EMPLOYEE_UPDATE: Changed as part of employee record update
   * - BULK_UPDATE: Changed via bulk salary update operation
   * - MIGRATION: Changed during data migration
   * - SYSTEM: Changed by system (future use)
   */
  source: "MANUAL" | "EMPLOYEE_UPDATE" | "BULK_UPDATE" | "MIGRATION" | "SYSTEM";

  /**
   * When the salary change was made (ISO date string)
   */
  changedAt: string;

  /**
   * UUID of the user who made the salary change
   * Used for audit trail and permission tracking
   */
  changedBy: string;

  /**
   * User details (populated when including relations)
   * Contains firstName, lastName, email for display
   */
  changedByUser?: {
    id: string;
    firstName: string;
    lastName: string;
    email?: string;
  };

  /**
   * ISO date string when this history record was created
   */
  createdAt: string;
}
